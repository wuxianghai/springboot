public class User {

    private int id;

    private String nick_name;

    private String sex ;

    private int age;

    private int height;

    private int weight;

    private String user_character;

    private String insterest;

    private String mobile;

    private String password;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNick_name() {
        return nick_name;
    }

    public void setNick_name(String nick_name) {
        this.nick_name = nick_name;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public String getUser_character() {
        return user_character;
    }

    public void setUser_character(String user_character) {
        this.user_character = user_character;
    }

    public String getInsterest() {
        return insterest;
    }

    public void setInsterest(String insterest) {
        this.insterest = insterest;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
